void init_matrice(void)
{
int i, j;
	for (j = 0; j<TY; j++){
		for (i = 0; i<TX; i++){
			MAT[j][i] = 0;
			SAV[j][i] = 0;
		}
	}
	MAT[TY / 2][TX / 2] = 1;
	MAT[TY / 2 + 1][TX / 2] = 1;
	MAT[TY / 2][TX / 2 + 1] = 1;
	MAT[TY / 2 + 1][TX / 2 + 1] = 1;
}
